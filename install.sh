section() {
    echo
    echo -e "${BOLD}${CYAN}==> $1${NC}"
}

main() {
    banner

    # Kill old processes (safe to run even if nothing is open)
    killall -9 Opiumware &>/dev/null || true
    killall -9 "Opiumware Old UI" &>/dev/null || true
    
    # Target path uses a different name so your current UI is untouched
    local target_app="$APP_DIR/Opiumware Old UI.app"
    
    # Remove existing "Opiumware Old UI.app" safely (if you are re-running the installer)
    if [ -e "$target_app" ]; then
        if rm -rf "$target_app" 2>/dev/null; then
            :
        else
            if sudo -n true 2>/dev/null; then
                echo -e "${INFO} Please enter your password (required to overwrite old UI):"
                sudo rm -rf "$target_app" 2>/dev/null || {
                    echo -e "${RED}${CROSS} Failed to delete old UI. Please manually delete it.${NC}"
                }
            else
                echo -e "${RED}${CROSS} Failed to delete old UI. Please manually delete it.${NC}"
                exit 1
            fi
        fi
    fi

    section "Downloading & Installing Opiumware Old UI"
    (
        # Download the UI Zip
        curl -# -L "$UI_URL" -o "$TEMP/OpiumwareUI.zip"
        
        # Extract it
        unzip -oq "$TEMP/OpiumwareUI.zip" -d "$TEMP"

        # Check if necessary workspace folders exist, and create them if they don't
        for folder in ~/Opiumware/workspace ~/Opiumware/autoexec ~/Opiumware/themes ~/Opiumware/modules; do
            if [ ! -d "$folder" ]; then
                mkdir -p "$folder"
            fi
        done
        
        # Move the newly extracted app and rename it to "Opiumware Old UI.app"
        mv -f "$TEMP/Opiumware.app" "$target_app" 2>/dev/null || true
        
        # Codesign the app so macOS allows it to run
        codesign --force --deep --sign - --identifier com.norbyv1.opiumware "$target_app" >/dev/null 2>&1
        
        # Reset permissions
        tccutil reset ScreenCapture com.norbyv1.opiumware >/dev/null 2>&1
    ) & spinner "Installing" $!

    section "Finishing installation"
    (
        rm -rf "$TEMP"
    ) & spinner "Finishing" $!

    echo
    echo -e "${GREEN}${BOLD}Installation complete.${NC}"
    
    # Open the Old UI
    open "$target_app"
}

main
